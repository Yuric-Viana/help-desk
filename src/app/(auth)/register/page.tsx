"use client";

import { Form } from "@/components/Form";
import { SocialAuth } from "@/components/SocialAuth";
import { Button } from "@/components/ui/button";
import { Field, FieldLabel } from "@/components/ui/field";
import { Input } from "@/components/ui/input";
import Link from "next/link";
import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";

import { RegisterFormData, registerSchema } from "@/schemas/register";
import { useRegister } from "@/hooks/useRegister";

export default function RegisterPage() {
  const { isLoading, registerUser, apiError } = useRegister();

  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<RegisterFormData>({
    resolver: zodResolver(registerSchema),
  });

  return (
    <div className="mx-6 pb-35 lg:mx-auto lg:w-100 lg:pb-25">
      <div className="border-app-gray-500 rounded-2xl border-2 p-6">
        <Form
          title="Crie sua conta"
          description="Informe seu nome, e-mail e senha"
          onSubmit={handleSubmit(registerUser)}
          className=""
        >
          <Field>
            <FieldLabel
              htmlFor="name"
              className="text-app-gray-300 text-[10px] uppercase"
            >
              Nome
            </FieldLabel>
            <Input
              id="name"
              autoComplete="off"
              {...register("name")}
              placeholder="Digite seu nome completo"
              className="placeholder:text-md placeholder:text-app-gray-400 border-app-gray-500 rounded-none border-0 border-b px-0 focus-visible:ring-0"
            />
            {errors.name && (
              <span className="text-xs text-red-500">
                {errors.name.message}
              </span>
            )}
          </Field>
          <Field>
            <FieldLabel
              htmlFor="email"
              className="text-app-gray-300 text-[10px] uppercase"
            >
              E-mail
            </FieldLabel>
            <Input
              id="email"
              autoComplete="off"
              {...register("email")}
              placeholder="exemplo@email.com"
              className="placeholder:text-md placeholder:text-app-gray-400 border-app-gray-500 rounded-none border-0 border-b px-0 focus-visible:ring-0"
            />
            {errors.email && (
              <span className="text-xs text-red-500">
                {errors.email.message}
              </span>
            )}
          </Field>
          <Field>
            <FieldLabel
              htmlFor="password"
              className="text-app-gray-300 text-[10px] uppercase"
            >
              Senha
            </FieldLabel>
            <Input
              id="password"
              autoComplete="off"
              {...register("password")}
              type="password"
              placeholder="Digite sua senha"
              className="placeholder:text-md placeholder:text-app-gray-400 border-app-gray-500 rounded-none border-0 border-b px-0 focus-visible:ring-0"
            />
            {!errors.password ? (
              <small className="text-app-gray-400 text-xs italic">
                Mínimo de 6 dígitos
              </small>
            ) : (
              <span className="text-xs text-red-500">
                {errors.password.message}
              </span>
            )}
          </Field>
          <Field>
            <FieldLabel
              htmlFor="passwordConfirm"
              className="text-app-gray-300 text-[10px] uppercase"
            >
              Confirme sua senha
            </FieldLabel>
            <Input
              id="passwordConfirm"
              autoComplete="off"
              {...register("confirmPassword")}
              type="password"
              placeholder="Digite sua senha"
              className="placeholder:text-md placeholder:text-app-gray-400 border-app-gray-500 rounded-none border-0 border-b px-0 focus-visible:ring-0"
            />

            {!errors.confirmPassword ? (
              <small className="text-app-gray-400 text-xs italic">
                Mínimo de 6 dígitos
              </small>
            ) : (
              <span className="text-xs text-red-500">
                {errors.confirmPassword.message}
              </span>
            )}
          </Field>
          {apiError && (
            <div className="rounded border border-red-500 bg-red-500/10 p-2 text-center text-sm text-red-500">
              {apiError}
            </div>
          )}
          <Field>
            <Button type="submit" variant="default" disabled={isLoading}>
              {isLoading ? "Cadastrando..." : "Cadastrar"}
            </Button>
          </Field>
        </Form>
        <SocialAuth subtitle="Ou cadastre-se com" />
        <p className="text-app-gray-400 mt-6 text-center text-sm italic">
          Já tem uma conta ?{" "}
          <Link className="text-app-gray-200" href="/">
            Entrar
          </Link>{" "}
        </p>
      </div>
    </div>
  );
}
